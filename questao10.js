/*
Questão 10: Como você pode remover o último elemento de um array em 
JavaScript?
*/

let meuArray = [1, 2, 3, 4, 5];
let ultimoElementoRemovido = meuArray.pop();
console.log(meuArray); // Isso imprimirá [1, 2, 3, 4] no console
console.log("Último elemento removido:", ultimoElementoRemovido); // Isso imprimirá "Último elemento removido: 5" no console
